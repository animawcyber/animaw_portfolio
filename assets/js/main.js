// Get the modal
var modal = document.getElementById("imageModal");

// Get the certification images and insert them inside the modal
var certificationImages = document.querySelectorAll(".certifications .work__img img");
var modalImg = document.getElementById("zoomedImage");

// Loop through each certification image and add an onclick event
certificationImages.forEach(function(img) {
  img.addEventListener("click", function() {
    modal.style.display = "block";
    modalImg.src = this.src;
  });
});

// Get the <span> element that closes the modal
var span = document.getElementsByClassName("close")[0];

// When the user clicks on <span> (x), close the modal
span.onclick = function() { 
  modal.style.display = "none";
}

// When the user clicks anywhere outside of the modal, close it
window.onclick = function(event) {
  if (event.target == modal) {
    modal.style.display = "none";
  }
}
